let nota = 'Aprovado';
switch (nota) {
    case "Aprovado":
        console.log('Parabéns você foi aprovado');
        break;

    case "Lista":
        console.log('Você está na lista de espera');
        break;

    case "Reprovada":
        console.log('Você foi reprovado');
    
    default:
        console.log('valor não identificado')
}